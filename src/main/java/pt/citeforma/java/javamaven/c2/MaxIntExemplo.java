package pt.citeforma.java.javamaven.c2;

public class MaxIntExemplo {
    public static void main(String[] args) {
        int i;
        i = Integer.MAX_VALUE;
        System.out.println("i = " + i);
        
        i++;
        System.out.println("i = " + i);
        
        int a, b;
        
        a = 3;
        b = 2;
        System.out.println(1.0 * a/b);
        System.out.println(a/b);
    }
}
