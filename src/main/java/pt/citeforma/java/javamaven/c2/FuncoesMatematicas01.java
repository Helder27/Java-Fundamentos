package pt.citeforma.java.javamaven.c2;

public class FuncoesMatematicas01 {
    
    public static void main(String[] args) {
        double d1 = 4;
        double d2;

        d2 = Math.sqrt(d1);
        System.out.println("Valor d2 = " + d2);
        System.out.println("Potencial = " + Math.pow(d1, d2));
        System.out.println("Seno = " + Math.sin((Math.PI/2.0)));
        System.out.println("Exponencial = " + Math.exp(1));
    }
}
